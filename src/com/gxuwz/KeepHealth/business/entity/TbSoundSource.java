package com.gxuwz.KeepHealth.business.entity;

/**
 * 声源实体类
 * @author Administrator
 *
 */
public class TbSoundSource implements java.io.Serializable {

	// Fields

	private Integer soundId;//声源编号
	private String soundName;//声源名称（中文）
	private Integer soundSex;//声源性别
	private Integer soundState;//声源状态0：未上线 1：已上线（默认上线）

	// Constructors

	/** default constructor */
	public TbSoundSource() {
	}

	/** full constructor */
	public TbSoundSource(String soundName, Integer soundSex, Integer soundState) {
		this.soundName = soundName;
		this.soundSex = soundSex;
		this.soundState = soundState;
	}

	// Property accessors

	public Integer getSoundId() {
		return this.soundId;
	}

	public void setSoundId(Integer soundId) {
		this.soundId = soundId;
	}

	public String getSoundName() {
		return this.soundName;
	}

	public void setSoundName(String soundName) {
		this.soundName = soundName;
	}

	public Integer getSoundSex() {
		return this.soundSex;
	}

	public void setSoundSex(Integer soundSex) {
		this.soundSex = soundSex;
	}

	public Integer getSoundState() {
		return this.soundState;
	}

	public void setSoundState(Integer soundState) {
		this.soundState = soundState;
	}

}